import stats from 'simple-statistics'

export function getDescriptiveStats(...nums: number[]) {
  const min = stats.min(nums)
  const max = stats.max(nums)
  const sum = stats.sum(nums)
  const mean = stats.mean(nums)
  const mode = stats.mode(nums)
  const median = stats.median(nums)
  const variance = stats.variance(nums)
  const standardDeviation = stats.standardDeviation(nums)
  return {
    max,
    mean,
    median,
    min,
    mode,
    standardDeviation,
    sum,
    variance
  }
}

export function getZScore(nums: number[], query: number) {
  const mean = stats.mean(nums)
  const standardDeviation = stats.standardDeviation(nums)
  const zScore = stats.zScore(query, mean, standardDeviation)
  return zScore
}

